package com.example.notepadauth;

/**
 * Class of the User containing their id, title, and notes
 */
public class User {
    private String id;
    private String title;
    private String note;

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getNote() {
        return note;
    }
}
